        <h4 class="accueil text-center mt-3 mb-2">Le mot du directeur</h4>
        <p class="justified-text text">
          Le semi-marathon de La Rochelle est une manifestation sportive et festive qui existe depuis 47
          ans dans notre ville.<br>
          <br>
          Un nouveau circuit roulant de deux boucles qui allie performance et plaisir touristique autour de
          ses portes et de son centre-ville historique. Du front de mer de Port Neuf, l’allée du Mail pour
          rejoindre les Quais du Vieux Port et ses Tours. Les derniers mètres sur la piste d’athlétisme au
          sein du village au Stade A. Bouffenie pour une arrivée magique et inoubliable.<br>
          <br>
          Une participation de 3500 coureurs dont une régularité de 1400 féminines présentes à l’arrivée.<br>
          <br>
          Depuis 3 ans, avec notre équipe, j’organise cette compétition pour la rendre pérenne, la
          dynamiser et en faire un rendez-vous annuel apprécié et attendu des athlètes et de nos
          partenaires.<br>
          N’hésitez plus, venez nous rejoindre.<br>
          </p> 
          
          <h5 class="accueil text-center mt-1 mb-1">Un événement sportif pour tous : performance, partage et santé</h5>

       <p class="justified-text text">
          Au-delà du semi-marathon, notre rendez-vous sportif encourage le partage de l’effort et la pratique du sport-santé.<br>
          <br>
          Deux formats complémentaires permettent à chacun de trouver sa place :<br>

          • le relais à deux, pour vivre l’épreuve en équipe et partager le plaisir de courir ensemble,<br>

          • le 5 km, accessible à tous, pour découvrir la course à pied, retrouver la forme ou se préparer à franchir un jour la distance du semi-marathon.<br>
          <br>
          Le semi-marathon de La Rochelle célèbre ainsi le sport comme vecteur de santé, de dépassement et de convivialité.<br>

          Comme chaque année depuis la 44ème édition, les participants recevront la médaille de finisher, symbole d’effort et de fierté.<br>
          <br>
          Le public est invité à venir nombreux encourager les coureurs et partager cette journée placée sous le signe du plaisir de courir ensemble.<br>
        </p><br>
